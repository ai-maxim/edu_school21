#include "libft.h"

void	ft_bzero(void *s, size_t len)
{
	unsigned char	*a;
	size_t			i;

	a = (unsigned char *)s;
	i = 0;
	while (i < len)
	{
		a[i] = '\0';
		i++;
	}
}
